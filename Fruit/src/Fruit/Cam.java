package Fruit;

public class Cam {
	private String ten;
	private int gia;
	private int soluong;
	private int cannang;
	public Cam(String ten, int gia, int soluong, int cannang) {
		super();
		this.ten = ten;
		this.gia = gia;
		this.soluong = soluong;
		this.cannang = cannang;
	}
	public String getTen() {
		return ten;
	}
	public int getGia() {
		return gia;
	}
	public int getSoluong() {
		return soluong;
	}
	public int getCannang() {
		return cannang;
	}
	
}
