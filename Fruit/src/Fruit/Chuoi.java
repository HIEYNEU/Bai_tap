package Fruit;

public class Chuoi {
	private String ten;
	private int gia;
	private int soluong;
	private int kali;
	public Chuoi(String ten, int gia, int soluong, int kali) {
		super();
		this.ten = ten;
		this.gia = gia;
		this.soluong = soluong;
		this.kali = kali;
	}
	public String getTen() {
		return ten;
	}
	public int getGia() {
		return gia;
	}
	public int getSoluong() {
		return soluong;
	}
	public int getKali() {
		return kali;
	}
	
}
