package Fruit;

public class Tao {
	private String ten;
	private int gia;
	private int soluong;
	private int luongduong;
	public Tao(String ten, int gia, int soluong, int luongduong) {
		super();
		this.ten = ten;
		this.gia = gia;
		this.soluong = soluong;
		this.luongduong = luongduong;
	}
	public String getTen() {
		return ten;
	}
	public int getGia() {
		return gia;
	}
	public int getSoluong() {
		return soluong;
	}
	public int getLuongduong() {
		return luongduong;
	}
	
}
