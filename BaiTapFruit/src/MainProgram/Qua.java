package MainProgram;

public class Qua {
	private String ten;
	private int gia;
	private int soluong;
	public Qua(String ten, int gia, int soluong) {
		super();
		this.ten = ten;
		this.gia = gia;
		this.soluong = soluong;
	}
	public String getTen() {
		return ten;
	}
	public int getGia() {
		return gia;
	}
	public int getSoluong() {
		return soluong;
	}
	
}
